package net.nan21.dnet.module.ad.usr.ds.converter;

import net.nan21.dnet.core.api.converter.IDsConverter;

import net.nan21.dnet.core.presenter.converter.AbstractDsConverter;
import net.nan21.dnet.module.ad.usr.ds.model.UserGroupDs;
import net.nan21.dnet.module.ad.usr.domain.entity.UserGroup;

public class UserGroupDsConv extends
        AbstractDsConverter<UserGroupDs, UserGroup> implements
        IDsConverter<UserGroupDs, UserGroup> {

    protected void modelToEntityAttributes(UserGroupDs ds, UserGroup e)
            throws Exception {
        e.setName(ds.getName());
        e.setActive(ds.getActive());
        e.setDescription(ds.getDescription());
        e.setClientId(ds.getClientId());
        e.setVersion(ds.getVersion());
    }

    protected void modelToEntityReferences(UserGroupDs ds, UserGroup e)
            throws Exception {
    }

    @Override
    public void entityToModel(UserGroup e, UserGroupDs ds) throws Exception {
        ds.setName(e.getName());
        ds.setActive(e.getActive());
        ds.setDescription(e.getDescription());
        ds.setId(e.getId());
        ds.setClientId(e.getClientId());
        ds.setCreatedAt(e.getCreatedAt());
        ds.setModifiedAt(e.getModifiedAt());
        ds.setCreatedBy(e.getCreatedBy());
        ds.setModifiedBy(e.getModifiedBy());
        ds.setVersion(e.getVersion());
    }

}
