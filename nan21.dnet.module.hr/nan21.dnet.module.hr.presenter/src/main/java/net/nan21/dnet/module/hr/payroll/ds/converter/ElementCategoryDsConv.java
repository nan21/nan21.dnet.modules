package net.nan21.dnet.module.hr.payroll.ds.converter;

import net.nan21.dnet.core.api.converter.IDsConverter;

import net.nan21.dnet.core.presenter.converter.AbstractDsConverter;
import net.nan21.dnet.module.hr.payroll.ds.model.ElementCategoryDs;
import net.nan21.dnet.module.hr.payroll.domain.entity.ElementCategory;

public class ElementCategoryDsConv extends
        AbstractDsConverter<ElementCategoryDs, ElementCategory> implements
        IDsConverter<ElementCategoryDs, ElementCategory> {

    protected void modelToEntityAttributes(ElementCategoryDs ds,
            ElementCategory e) throws Exception {
        e.setName(ds.getName());
        e.setActive(ds.getActive());
        e.setDescription(ds.getDescription());
        e.setClientId(ds.getClientId());
        e.setVersion(ds.getVersion());
    }

    protected void modelToEntityReferences(ElementCategoryDs ds,
            ElementCategory e) throws Exception {
    }

    @Override
    public void entityToModel(ElementCategory e, ElementCategoryDs ds)
            throws Exception {
        ds.setName(e.getName());
        ds.setActive(e.getActive());
        ds.setDescription(e.getDescription());
        ds.setId(e.getId());
        ds.setClientId(e.getClientId());
        ds.setCreatedAt(e.getCreatedAt());
        ds.setModifiedAt(e.getModifiedAt());
        ds.setCreatedBy(e.getCreatedBy());
        ds.setModifiedBy(e.getModifiedBy());
        ds.setVersion(e.getVersion());
    }

}
